local PlayersUsed = {}
function SWEP:PrimaryAttack()
    self:SetHoldType("slam")
    
	local trace = self:GetOwner():GetEyeTrace()
	if not IsValid(trace.Entity) or not trace.Entity:IsPlayer() or trace.Entity:GetPos():Distance(self:GetOwner():GetPos()) > 100 then
		return
	end
	
    if SERVER then
    if CurTime() < self.NextStrike then
        DarkRP.notify(self:GetOwner(), 0, 4, "Списать налог можно через " .. math.Round(self.NextStrike - CurTime()) .. " секунд")
        return
    end
    end


	self:EmitSound("npc/combine_soldier/gear4.wav", 50, 100)

	if SERVER then
		if self:GetOwner():Team() ~= TEAM_NALOG then self:Remove() return end
		if (trace.Entity:Team() == TEAM_ADMIN or trace.Entity:Team() == TEAM_MAYOR) then self:GetOwner():ChatPrint('Ты не можешь списывать с него налог!') return end
		if not trace.Entity:canAfford(100000) then self:GetOwner():ChatPrint(trace.Entity:Nick()..' находится в списке малоимущих! С него нельзя списать налог.') return end
		
		local donenalog = PlayersUsed[ trace.Entity:SteamID() ]
		if donenalog then self:GetOwner():ChatPrint(trace.Entity:Nick()..' уже оплатил налог!') return end
		
		local percent = math.Clamp(math.floor(trace.Entity:getDarkRPVar("money") / 100 * 0.5), 500, 150000)
		local result = math.floor(percent / 4)
		
		trace.Entity:addMoney(-percent)
		trace.Entity:ChatPrint("[Налог] С вас списали налог в размере "..percent.."$, с этой суммы 25% уходит сотруднику")
		
		self:GetOwner():addMoney(result)
		self:GetOwner():ChatPrint("[Налог] Ты списал с "..trace.Entity:Nick().." налог в размере "..percent.."$ ваша прибыль "..result.."$")
		
		if #team.GetPlayers(TEAM_MAYOR) == 1 then
			for k,v in pairs(team.GetPlayers(TEAM_MAYOR)) do
				v:addMoney(result)
				v:ChatPrint("[Налоговая инспекция] Вы получили "..result.."$ с налоговой декларации "..trace.Entity:Nick())
			end
		end

		PlayersUsed[ trace.Entity:SteamID() ] = true
		self.NextStrike = CurTime() + 150
